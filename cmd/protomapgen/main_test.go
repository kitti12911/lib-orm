func TestLoadConfigRoundtrip(t *testing.T) {
	dir := t.TempDir()
	configPath := filepath.Join(dir, "protomapgen.yaml")
	require.NoError(t, os.WriteFile(configPath, []byte(`
targets:
  - go_dir: internal/database
    go_import: example.com/svc/internal/database
    go_type: User
    proto_import: example.com/svc/gen/grpc/user/v1
    package: user
    out: internal/feature/user/mapper_generated.go
    exclude: [DeletedAt]
    converters:
      Status:
        to_proto: statusToProto
        from_proto: statusFromProto
`), 0o600))

	cfg, err := loadConfig(configPath)
	require.NoError(t, err)
	require.Len(t, cfg, 1)

	tgt := cfg[0]
	assert.Equal(t, "internal/database", tgt.GoDir)
	assert.Equal(t, "User", tgt.GoType)
	assert.Equal(t, "User", tgt.ProtoType, "proto_type defaults to go_type")
	assert.Equal(t, "database", tgt.GoAlias, "go_alias derived from import path")
	assert.Equal(t, "userv1", tgt.ProtoAlias, "proto_alias derived from import path")
	assert.Equal(t, dirBoth, tgt.Direction)
	assert.Equal(t, "UserToProto", tgt.FuncToProto, "func_to_proto defaults to <type>ToProto")
	assert.Equal(t, "UserFromProto", tgt.FuncFromProto)
	assert.True(t, tgt.TargetPointer)
	assert.True(t, tgt.Exclude["DeletedAt"])
	assert.Equal(t, "statusToProto", tgt.Converters["Status"].ToProto)
}

