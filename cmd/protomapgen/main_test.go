func TestLoadConfigConverterRequiresMatchingDirection(t *testing.T) {
	dir := t.TempDir()
	// from_proto-only direction: converter needs only from_proto.
	configPath := filepath.Join(dir, "protomapgen.yaml")
	require.NoError(t, os.WriteFile(configPath, []byte(`
targets:
  - go_dir: internal/database
    go_import: example.com/svc/internal/database
    go_type: User
    proto_import: example.com/svc/gen/grpc/user/v1
    direction: from_proto
    package: user
    out: mapper.go
    converters:
      Status:
        from_proto: statusFromProto
`), 0o600))

	_, err := loadConfig(configPath)
	require.NoError(t, err, "from_proto-only converter should be accepted when direction is from_proto")

	// direction=both: half-converter rejected.
	require.NoError(t, os.WriteFile(configPath, []byte(`
targets:
  - go_dir: internal/database
    go_import: example.com/svc/internal/database
    go_type: User
    proto_import: example.com/svc/gen/grpc/user/v1
    package: user
    out: mapper.go
    converters:
      Status:
        to_proto: statusToProto
`), 0o600))

	_, err = loadConfig(configPath)
	require.Error(t, err)
	assert.Contains(t, err.Error(), "needs both to_proto and from_proto")
}
