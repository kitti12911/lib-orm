func writeFile(t *testing.T, body string) string {
	t.Helper()
	dir := t.TempDir()
	require.NoError(t, os.WriteFile(filepath.Join(dir, "src.go"), []byte(body), 0o600))
	return dir
}

func bunBaseConfig(dir string) targetConfig {
	return targetConfig{
		GoDir:         dir,
		GoImport:      "example.com/svc/internal/database",
		GoAlias:       "database",
		GoType:        "Example",
		ProtoImport:   "example.com/svc/gen/grpc/example/v1",
		ProtoAlias:    "examplev1",
		ProtoType:     "Example",
		Direction:     dirBoth,
		FuncToProto:   "ExampleToProto",
		FuncFromProto: "ExampleFromProto",
		TargetPointer: true,
		Package:       "feature",
		Out:           filepath.Join(dir, "mapper_generated.go"),
		Exclude:       map[string]bool{},
		Converters:    map[string]converterPair{},
	}
}

func TestGenerateBunMode(t *testing.T) {
	dir := writeFile(t, bunModel)
	fields, isBun, err := parseStruct(dir, "Example")
	require.NoError(t, err)
	require.True(t, isBun)

	src, err := generate(bunBaseConfig(dir), fields, isBun)
	require.NoError(t, err)
	out := string(src)

	// Scalars and pointer-scalars are mapped directly (column → PascalCase).
	assert.Contains(t, out, "Id:          src.ID")
	assert.Contains(t, out, "DisplayName: src.DisplayName")
	assert.Contains(t, out, "ID:          in.Id")

	// time.Time is bridged through timestamppb.
	assert.Contains(t, out, "timestamppb.New(src.CreatedAt)")
	assert.Contains(t, out, "in.CreatedAt.AsTime()")

	// Untouched relations get a TODO marker.
	assert.Contains(t, out, "// TODO: Owner (*Example, relation)")

	// File header + both directions.
	assert.True(t, strings.HasPrefix(out, "// Code generated by protomapgen; DO NOT EDIT."), out)
	assert.Contains(t, out, "func ExampleToProto(")
	assert.Contains(t, out, "func ExampleFromProto(")
}

func TestGenerateBunModeConvertersAndExclude(t *testing.T) {
	dir := writeFile(t, bunModel)
	fields, isBun, err := parseStruct(dir, "Example")
	require.NoError(t, err)
	require.True(t, isBun)

	cfg := bunBaseConfig(dir)
	cfg.Converters["Status"] = converterPair{ToProto: "statusToProto", FromProto: "statusFromProto"}
	cfg.Converters["Owner"] = converterPair{ToProto: "ownerToProto", FromProto: "ownerFromProto"}
	cfg.Exclude["DisplayName"] = true

	src, err := generate(cfg, fields, isBun)
	require.NoError(t, err)
	out := string(src)

	assert.Contains(t, out, "statusToProto(src.Status)")
	assert.Contains(t, out, "statusFromProto(in.Status)")
	assert.Contains(t, out, "ownerToProto(src.Owner)")
	assert.Contains(t, out, "ownerFromProto(in.Owner)")
	assert.NotContains(t, out, "DisplayName")
	assert.NotContains(t, out, "// TODO: Owner")
}

func TestGeneratePlainModeReturnsValueType(t *testing.T) {
	dir := writeFile(t, plainStruct)
	fields, isBun, err := parseStruct(dir, "CreateParams")
	require.NoError(t, err)
	require.False(t, isBun)

	cfg := targetConfig{
		GoDir:         dir,
		GoImport:      "example.com/svc/internal/feature/user",
		GoAlias:       "user",
		GoType:        "CreateParams",
		ProtoImport:   "example.com/svc/gen/grpc/user/v1",
		ProtoAlias:    "userv1",
		ProtoType:     "CreateUserRequest",
		Unwrap:        "User",
		Direction:     dirFromProto,
		FuncFromProto: "createParamsFromProto",
		TargetPointer: false,
		Package:       "user",
		Out:           filepath.Join(dir, "mapper_generated.go"),
		Exclude:       map[string]bool{},
		Converters: map[string]converterPair{
			"Status":  {FromProto: "userStatusFromProto"},
			"Profile": {FromProto: "createProfileParamsFromProto"},
		},
	}

	src, err := generate(cfg, fields, isBun)
	require.NoError(t, err)
	out := string(src)

	// Value-type return uses zero-value fallback, not nil. Alias matches GoAlias.
	assert.Contains(t, out, "return user.CreateParams{}")
	assert.Contains(t, out, "dst := user.CreateParams{")
	// Direction filter omits the to-proto function entirely.
	assert.NotContains(t, out, "func CreateParamsToProto")
	// Field names match by identity (plain mode), no PascalCase conversion.
	assert.Contains(t, out, "Email:       src.Email")
	// Status / Profile converters are wired up.
	assert.Contains(t, out, "userStatusFromProto(src.Status)")
	assert.Contains(t, out, "createProfileParamsFromProto(src.Profile)")
}

func TestGenerateSelfPackageOmitsImport(t *testing.T) {
	dir := writeFile(t, plainStruct)
	fields, isBun, err := parseStruct(dir, "CreateParams")
	require.NoError(t, err)

	cfg := targetConfig{
		GoDir:         dir,
		GoImport:      "example.com/svc/internal/feature/user",
		GoAlias:       "user",
		GoType:        "CreateParams",
		GoSelf:        true, // out lives in the same package as the source struct
		ProtoImport:   "example.com/svc/gen/grpc/user/v1",
		ProtoAlias:    "userv1",
		ProtoType:     "CreateUserRequest",
		Unwrap:        "User",
		Direction:     dirFromProto,
		FuncFromProto: "createParamsFromProto",
		TargetPointer: false,
		Package:       "user",
		Out:           filepath.Join(dir, "mapper_generated.go"),
		Exclude:       map[string]bool{},
		Converters: map[string]converterPair{
			"Status":  {FromProto: "userStatusFromProto"},
			"Profile": {FromProto: "createProfileParamsFromProto"},
		},
	}

	src, err := generate(cfg, fields, isBun)
	require.NoError(t, err)
	out := string(src)

	// Self-package: no import of the source struct's package, bare type names.
	assert.NotContains(t, out, `"example.com/svc/internal/feature/user"`)
	assert.NotContains(t, out, "user.CreateParams")
	assert.Contains(t, out, "return CreateParams{}")
	assert.Contains(t, out, "dst := CreateParams{")
}

