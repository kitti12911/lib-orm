func TestProtoGoName(t *testing.T) {
	cases := map[string]string{
		"id":           "Id",
		"user_id":      "UserId",
		"display_name": "DisplayName",
		"line1":        "Line1",
		"":             "",
	}
	for input, want := range cases {
		t.Run(input, func(t *testing.T) {
			assert.Equal(t, want, protoGoName(input))
		})
	}
}

